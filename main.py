skills = [
    'Стремительный прыжок',
    'Электрический выстрел',
    'Ледяной удар',
    'Стремительный удар',
    'Кислотный взгляд',
    'Тайный побег',
    'Ледяной выстрел',
    'Огненный заряд'
]


def create_template():
    skills_choice = random.sample(skills, 3)
    runic_skills = []

    for skill in skills_choice:
        for letter, replacement in dict.items():
            skill = skill.replace(letter, replacement)
        runic_skills.append(skill)

    context = {
        'first_name': fake.first_name(),
        'last_name': fake.last_name(),
        'town': fake.job(),
        'job': fake.city(),
        'strength': str(random.randint(3, 18)),
        'agility': str(random.randint(3, 18)),
        'endurance': str(random.randint(3, 18)),
        'intelligence': str(random.randint(3, 18)),
        'luck': str(random.randint(3, 18)),
        'skill_1': runic_skills[0],
        'skill_2': runic_skills[1],
        'skill_3': runic_skills[2]
    }
    return context


def main():
    for i in range(10):
        context = create_template()
        from_file = 'src/template.svg'
        to_file = f'src/output/result_{i + 1}.svg'
        file_operations.render_template(from_file, to_file, context)


if __name__ == '__main__':
    main()
